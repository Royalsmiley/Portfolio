			<p> <h1> Scrum  </h1>
			Scrum was een vrijwel cruciale factor in het werkproces voor mezelf en mijn proftaak groep, aangezien ik scrum ook heb gebruikt voor individueel proces. Scrum hielp mij en mijn proftaak groep veel in gestructureerd werken, hierdoor hadden we een simpel, flexibel en duidelijk overzicht over de taken die nog gedaan moeten worden, de taken waar iedereen mee bezig is en de taken die al af zijn. Scrum is een van de agile frameworks en verhoogt de effectiviteit. 
<br><br>
<b>Persoonlijke scrum</b><br>
Persoonlijke scrum was ook wel handig, dit was natuurlijk niet heel uitgebreid maar meer een klein lijstje met wat er nog gedaan moest worden en wat ik al gedaan had, hierdoor had ik een duidelijk overzicht van mijn taken maar hierdoor kreeg ik ook een meer voldaan gevoel als ik een taak af had, omdat dan het lijstje met done groter werd. Ik merkte goed dat ik hierdoor minder vaak iets vergat of later dan gepland iets af had.
<br><br>
<b>Proftaak scrum</b><br>
Proftaak scrum verliep vrij goed, in sprint 1 was ik de scrummaster. Als scrummaster heb je meer verantwoordelijkheid over de gang van zaken en in het begin moest ik hier nog wel even aan wennen, mijn inzet tijdens de daily stand-up was in het begin ook iets te wisselend. Zo was had ik de ene keer meer inzet dan de andere. In de loop van de sprint ging dit wel steeds beter en nam ik ook meer het initiatief en nam steeds beter het woord, aangezien we in een groep werken waar iedereen input heeft en zijn zegje mag doen liet ik soms mensen over me heen spreken. Bij de retrospective van sprint 1 kreeg ik bruikbare feedback van mijn groep. Deze feedback was wel grotendeels positief maar ik denk dat ik noujuist van de negatieve feedback meer kan leren, deze feedback heb ik goed in me opgeslagen en lette er ook op bij het allerdaags scrum. De retrospective was ook een fijne manier om feedback te krijgen of te ontvangen, hier geef je namelijk feedback op een iets professionele manier. Dit was heel handig was in onze groep aangezien wij een ook een vriendengroep zijn dus dan word het lastiger om kritische feedback te geven, gelukkig is de retrospective daarvoor perfect. Mijn ervaring als scrummaster heeft me geholpen om meer initiatief te nemen over het algemeen. 
<br><br>
In de rest van de sprints merkte ik een grote verbetering in mijn bijdrage bij scrum, ik nam zelfs zonder dat ik scrummaster was steeds meer iniatief. Ik nam taken op me eerder op me en hield iedereen, tijdens scrum, op de hoogte van mijn taken. Iniatief tonen is toch duidelijk het punt wat verbeterd is maar ook ben ik steeds meer actiever betrokken in plaats van dat ik stiller ben en amper iets zeg zoals voorheen. Bij de proftaak heb ik nog nooit het probleem gehad dat ik iets niet op tijd af had aangezien je bij scrum dit snel kan doorgeven, dit was ook mede omdat we bij elke taak vaak besproken hoelang deze ongeveer zou duren.
<br><br>
<b>Terug blik naar eerder Scrum</b>
<br>
In S1 en S3 heb ik ook gebruik gemaakt van scrum, beide deze keren ging het minder dan afgelopen keer waardoor ik goed merk dat ik scrum veel beter begrijp en kan toepassen. De Scrum in S1 was echter heel minimaal, meer een lijstje van taken die we nog moesten doen, lijstje met taken waarmee we bezig waren en een lijstje van de taken die al klaar waren. Als je moet toen had gevraagd wat een retrospective was had ik er geen antwoord op kunnen geven, daardoor merk ik dat mijn kennis over scrum en alle begrippen die daarbij horen flink vergoot is.
<br><br>
De scrum van S3 was al een stuk beter dan die uit S1 maar nog steeds niet optimaal, bij de scrum van S3 hadden we namelijk geen goed gestructureerde daily standup en retrospective was ook nergens te bekennen. Het scrum werd toen veel geleid door de scrummaster maar ik merkte dat de betrokkenheid niet veel was, waarschijnlijk omdat het allemaal zo rommelig verliep. Daarom vind ik dat ons scrum dit semester flink is verbeterd, wij hadden een vaste tijd voor de dailystandup met duidelijke regels en afspraken zodat nu niet alleen de scrummaster betrokken was maar iedereen van het groepje.
<br><br>
<b>Userstories</b>
<br>
De meeste userstories die wij hebben opgesteld zijn allemaal gebaseerd op de website. Dit komt omdat we pas userstories hebben toegepast toen we net begonnen met de website. Met deze userstories heb ik niet veel gedaan met mijn taken aangezien de meeste userstories terug slaan op het design van de website, maar het design van de website was de taak van iemand anders uit de proftaak. Hierna ben ik nog wel een keer het design afgelopen om te kijken of de userstories wel kloppen vergeleken met de website. <br><br>
Maar later in het proces begonnen we de stories steeds meer te gebruiken, dit scheelde veel wanneer we iets als 'done' beschouwde. Hiervoor hadden we voorheen geen duidelijk regels voor maar sinds de userstories gebruiken we die om te kijken of elke taak wel overeen met de userstories.

