            Bij Digital Publishing en SEO heb ik beide gebruikt gemaakt van dezelfde framework, namelijk bootstrap. Bootstrap is vrij handig omdat het veel werk scheelt als het gaat om vormgeving, het is handig om snel een website te maken voor projecten waarbij het zelf coderen van de website niet van belang is. Dit scheelt veel tijd waardoor je meer tijd hebt om te focusen op andere aspecten van de opdracht bijvoorbeeld.<br><br>
