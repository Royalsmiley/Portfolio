			<p> <h1> Strategy & Concepting  </h1>
				Strategie en concept was heel belangrijk in het begin van de proftaak aangezien we natuurlijk begonnen met de concept fase. In de conceptfase van de proftaak moest er nog veel onderzoek worden gepleegd. In het begin gingen we concepten verzinnen en de doelgroep analyseren.
<br><br>
<b>Doelgroep analyse</b><br>
De doelgroep vaststellen was in grote termen makkelijk, 50+, maar we wilden graag het allemaal goed analyseren en kijken naar een aantal specifieke doelgroepen. Alles over de doelgroep analyse staat in het communicatiestrategie, hierin staan veel van alle onderzoeken die gedaan zijn. Hiervan zijn enkele onderzoeken samen gedaan door mij en mijn groepsgenoten volgens verschillende onderzoeksmethodes. Zoals bij de doelgroep hebben we veel informatie verkregen door veld en bieb onderzoek. Vervolgens heb ik zelfstandig een 'ideal scenario' gemaakt voor ons concept, dit staat onder het kopje "storyline van het concept" Voor als je het hele bestand wil bekijken heb je hier de <a href="http://rvandewiel.com/Communicatiestrategie.pdf" target="_blank">communicatiestrategie</a> . 
<br><br>
<b>Concepting</b><br>
We hadden we een totaal van acht concepten om uit te kiezen, hieruit kozen wij er drie en gingen hier onderzoek naar doen. We splitsen de proftaak op in groepjes om de drie concepten uit te werken en ik koos ervoor om het nostalgie concept uit te werken, na wat onderzoek stelden we een document op. In de les hadden we geleerd over het slapend, ontwakend en levend concept. Bij deze les moesten we eerst in tweetallen het hele proces van slapend, ontwakend en levend concept creeeren, als onderwerp had ik ons nostalgie concept. Dit concept ging ik verder uitwerken en uiteindelijk heb ik een document hiervan opgestelt:
<a href="http://rvandewiel.com/nostal.pdf" target="_blank">Onderzoeksdocument nostalgie</a>.
<br><br>
Nadat we de concepten hadden uitgewerkt hadden we een presentatie bij Perplex. Al vlug kwam tevoorschijn dat ambassadeurs de keuze had en gingen wij vlug aan de slag in sprint 1 om dit concept verder uit te werken. Dus wij maakten nog een uitgeschreven versie met de techniek die we eerder ook al hadden toegepast. 
<br><br>
<b>Middelen</b><br>
Vervolgens gingen we de middelen verder uitwerken en hierbij gingen we vast stellen wat nou het doel is en wat me ermee willen bereiken, de middelen zijn namelijk cruciaal aangezien je zonder middelen niks kan met je concept. Een van de belangrijkste middelen is de kaart, dit is ons hoofd middel, hiervoor moest nog wel een visual gemaakt worden en documentatie erbij.
<br><br>
<b>Contentstrategie</b><br>
Natuurlijk is de content ook belangrijk, de inhoud van wat we willen overbrengen, dit hebben wij vastgesteld in een contentstrategie die gemaakt is door mij en Nick. Dit was de eerste keer dat ik een contentstrategie heb gemaakt maar ik heb ervan geleerd, zo is de customer journey ook een heel sterk aspect aangezien je hiermee de verschillende middelen kan koppelen aan een tijdbalk. De doelgroep/klant heeft namelijk een proces die hij/zij verloopt, deze staat helemaal beschreven in het <a href="http://rvandewiel.com/Contentstrategie.pdf" target="_blank">contentstrategie document </a>
<br><br>
<b>Customer journey</b><br>
Voor de proftaak hadden we besloten om ook een Customer journey te maken zodat het duidelijk is wat het proces is wat de gebruiker doorloopt en welke middelen in welk stadia worden gebruikt. We gingen even goed kijken wat er tijdens welke stadia aan bod kwam en dankzij de vooronderzoeken bij onze middelen was dit geen probleem. Het leek mij slim om een visualisatie te maken van de customer journey om zo duidelijker te laten zien hoe het in zijn werking gaat. <br>
            -<a href="http://rvandewiel.com/images/sb-journey.png" target="_blank">Storyboard customer journey </a>
<br><br>
<b>Deze website</b><br>
SCO heb ik bijvoorbeeld ookal lichtelijk toegepast op mijn website, zo is er bewust nagedacht over de meeste aspecten van de website. Zo is het lettertype en stijl beide zo gekozen, deze zijn zo gekozen om mij als persoon reflecteren en om een totaal plaatje te creeren. Voor de rest is dit portfolio zo opgesteld zodat het makkelijk overzichtbaar maar ook voldoende informatie.
