			<p> <h1> User experience  </h1>
				User experience had veel effect op ons concept, met user centered design hebben we verschillende onderzoeken gedaan. 
				<br><br>
				
				voor UX kregen we ook een stock kaarten, wij kregen een volgorde waarop de gebruiker de kaarten moest neerlegen, deze volgorde moesten wij doorgeven aan de gebruiker door middel van plaatjes of iets in die richting (zolang we maar niet letterlijk neerzetten wat ze moesten pakken). Frans en ik kregen al vlug het idee om romeinse cijfers te gebruiken als kaartnummer, en voor de koning een kroon, hierdoor hadden we al snel een duidelijke volgorde. Als laatste tekenden we nog een groot hart erbij (groter dan al het andere) om aan te duiden dat het harten was (en geen schoppen, klaveren of ruiten).
				<br><br>
				Vervolgens moesten wij het testen, eigenlijk met andere klasgenoten maar het leek ons beter om iemand buiten de klas te pakken die niks weet van deze opdracht. Dus wij liepen een klas van eerstejaars binnen en gaven een jongen de stok kaarten en gaven hem de aanwijzingen. Het duurde niet lang of hij begon de kaarten op de juiste volgorde te leggen, wij waren erg blij met dit resultaat aangezien dit het ideale resultaat was.
				<br><br>
				<b>Interviews</b><br>
				In de proftaak hadden we informatie nodig over de werknemers van specsavers, hoe hun in hun schoenen staan etc om achter deze informatie te komen was het van belang dat we een veldonderzoek doen, wij deden dit veldonderzoek in de vorm van interviews. Vervolgens ging ik in totaal drie werknemers interviewen van verschillende filialen en vervolgens verwerkten we dit verder in de proftaak zoals bijvoorbeeld persona’s. Voordat ik het interview ging afnemen had ik een interview document gemaakt, daarin staat het onderzoeksdoel, plan van aanpak en natuurlijk de interviewvragen zelf. De drie interviews die ik heb afgenomen heb ik tevens ook opgenomen.
				<br><a href="http://rvandewiel.com/Interviewdocument.pdf" target="_blank">Interview document</a>
