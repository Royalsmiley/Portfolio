				<br><br>
				<b>Paper prototype</b><br>
				Tevens heb ik bij DP een paper-prototype gemaakt voor ons applicatie (zie afbeelding hier rechts). Het is een low fidelity prototype met als doel om te kijken of interactie met de app duidelijk en overzichtelijk is, als dit niet het geval is dan zouden we de interface moeten aanpassen. 
				<br><br>
				<b>Material design</b><br>
				Bij UX kregen we ook een les over material design, voorheen had ik eigenlijk nog niet gehoord van material design dus het was iets nieuws voor me (zie afbeelding hier rechts). Vervolgens kregen we de opdracht om een material design te maken van een bordspel, ik koos mens erger je niet. na wat meer onderzoek ging ik aan de slag en creëerde zo goed mogelijk een material design.
                <br><br>
				<b>User tests</b><br>
                User Tests is een cruciaal aspect van UX, user tests zijn zeer handig om informatie over een product te krijgen. Bij user tests is het natuurlijk ook heel belangrijk dat er genoeg voorbereiding is getroffen, er moeten vragen en enkele andere gegevens bedacht worden. Als het voorbereidings document klaar is dan kunnen de user tests worden afgenomen, bij het kiezen van testgebruikers is het belangrijk dat ze in het vooraf gemaakte  criteria vallen.
                <br><br>
                Vervolgens is het van belang dat de feedback en antwoorden van de testgebruikers goed worden uitgewerkt en verwerkt in de conclusie.
                Wanneer ik feedback kreeg van de docent was het ook van belang dat er bovenaan het document ook nog een kleine conclusie staat bovenaan het document. Mocht je het hele document willen lezen: <a href="http://rvandewiel.com/testrapport.pdf" target="_blank">testrapport</a><br><br>
