				<b>Storyboards</b><br>
				Prototypen zijn ook een belangrijk aspect van user experience, storyboard zijn een prototyping techniek dat de functionaliteit simuleert. dit was belangrijk voor zowel de proftaak maar ook mijn innovatieroute, Digital Publishing (zie afbeeldingen hier rechts). Voor de proftaak had ik een storyboard gemaakt voor het ideal user scenario, dit heeft als doel het testen van een van onze middelen, de kaart, hierdoor weten we concreet hoe we het willen hebben. voor meer storyboards: <br>					
